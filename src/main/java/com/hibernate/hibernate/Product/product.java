package com.hibernate.hibernate.Product;

import jakarta.persistence.*;

@Entity
@Table
public class product {
    @Id

    private int id;
    private String name;
    @Column(name="discription")
    private String desc;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
